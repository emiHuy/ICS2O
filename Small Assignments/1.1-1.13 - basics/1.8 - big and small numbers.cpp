//Working with big and small numbers
#include <iostream>
using namespace std;
int main() {
  //big numbers
  double bignumber1 = 200000000;
  double bignumber2 = 3000000000;
  cout << bignumber1 * bignumber2;
  //small numbers
  double smallnumber1 = 0.000000002;
  double smallnumber2 = 0.0000000003;
  cout << "\n\n" << smallnumber1 * smallnumber2;
  return 0;
  }